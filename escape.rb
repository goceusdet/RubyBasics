puts "Hello Ruby, what's up?"
# by puting \, whatever is after the / sign Ruby will print
puts 'Hello Ruby, what\'s up?' # result: Hello Ruby, what's up?
puts 'I\'m 6\'4"' # result: I'm 6'4"
puts "\t I'm tabbed" # tabbed space:          I'm tabbed
puts "Hello Ruby, \nwhat's up?" # \n places code after is in the next line.

