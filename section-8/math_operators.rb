# Assume variable a holds 10 and variable b holds 20
a = 10
b = 20

# Addition - adds values on either side of the operator
puts a + b # will give 30

# Substraction - substracts right operand from left one
puts b - a # will give 10

# Multiplication - multiplies values on either side of the operator
puts a * b # will give 200

# Division - divides left hand operand by right one
puts b / a # will give 2

# Modulus - divides left hand operand by right one and returns remainder
puts b % a # will give 0

# Exponent - performs exponential (power) calculation on operators
puts a**b # will give 100000000000000000000